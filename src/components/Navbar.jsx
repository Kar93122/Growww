import { useState, useRef, useEffect } from 'react';
import { useNavigate } from 'react-router-dom';
import { useAuth } from '../context/AuthContext';
import { useTheme } from '../context/ThemeContext';
import { signOut } from '../firebase/authService';

export default function Navbar({ onSearch }) {
  const { currentUser, userProfile } = useAuth();
  const { theme, toggle } = useTheme();
  const [menuOpen, setMenuOpen] = useState(false);
  const menuRef = useRef(null);
  const navigate = useNavigate();

  const handleSignOut = async () => { await signOut(); navigate('/auth'); };

  const initials = (userProfile?.displayName || currentUser?.email || 'U')
    .split(' ').map(w => w[0]).join('').toUpperCase().slice(0, 2);

  // Close dropdown on outside click
  useEffect(() => {
    const handler = (e) => {
      if (menuRef.current && !menuRef.current.contains(e.target)) setMenuOpen(false);
    };
    document.addEventListener('mousedown', handler);
    return () => document.removeEventListener('mousedown', handler);
  }, []);

  return (
    <nav className="navbar">
      {/* Search */}
      <div className="navbar-search">
        <span className="navbar-search-icon"><SearchIcon /></span>
        <input
          placeholder="Search stocks, crypto, bonds…"
          onChange={e => onSearch?.(e.target.value)}
        />
      </div>

      <div className="navbar-spacer" />

      <div className="navbar-actions">
        {/* ── Theme Toggle ── */}
        <button
          className="theme-toggle-btn"
          onClick={toggle}
          aria-label={`Switch to ${theme === 'dark' ? 'light' : 'dark'} mode`}
          title={`Switch to ${theme === 'dark' ? 'light' : 'dark'} mode`}
        >
          {theme === 'dark' ? <SunIcon /> : <MoonIcon />}
        </button>

        {/* ── Notifications ── */}
        <button className="navbar-icon-btn" title="Notifications">
          <BellIcon />
        </button>

        {/* ── Avatar + Dropdown ── */}
        <div style={{ position: 'relative' }} ref={menuRef}>
          <button className="avatar-btn" onClick={() => setMenuOpen(o => !o)}>
            {currentUser?.photoURL
              ? <img src={currentUser.photoURL} alt="avatar" />
              : initials}
          </button>

          {menuOpen && (
            <div className="nav-dropdown">
              <div className="nav-dropdown-header">
                <div className="nav-dropdown-name">
                  {userProfile?.displayName || 'Trader'}
                </div>
                <div className="nav-dropdown-email">{currentUser?.email}</div>
              </div>
              <button className="nav-dropdown-item" onClick={() => { navigate('/profile'); setMenuOpen(false); }}>
                <UserIcon /> Profile
              </button>
              <button className="nav-dropdown-item" onClick={() => setMenuOpen(false)}>
                <SettingsIcon /> Settings
              </button>
              <div className="nav-dropdown-divider" />
              <button className="nav-dropdown-item danger" onClick={handleSignOut}>
                <LogOutIcon /> Sign Out
              </button>
            </div>
          )}
        </div>
      </div>
    </nav>
  );
}

